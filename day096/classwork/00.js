
    const photo = document.getElementById('photo');
    const title = document.getElementById('title');

    function changeWidth() {
      photo.style.width = photo.style.width = '300px'
    }

    function changeHeight() {
      photo.style.height = photo.style.height =  '400px'
    }

    function changeRadius() {
      photo.style.borderRadius = photo.style.borderRadius =  '20px'
    }

    function changeShadow() {
      photo.style.boxShadow = '0 4px 20px rgba(0,0,0,0.3)';
    }

    function changeTitleColor() {
      title.style.color = title.style.color = 'darkblue'
    }

    function changeTitleSize() {
      title.style.fontSize = title.style.fontSize =   '28px'
    }

   
    title.style.fontSize = '40px';